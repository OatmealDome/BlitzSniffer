namespace Blitz.Sead
{
    public class Color4f
    {
        public float Red
        {
            get;
            set;
        }

        public float Green
        {
            get;
            set;
        }

        public float Blue
        {
            get;
            set;
        }

        public float Alpha
        {
            get;
            set;
        }

        public Color4f(float r, float g, float b, float a)
        {
            Red = r;
            Green = g;
            Blue = b;
            Alpha = a;
        }

    }
}
